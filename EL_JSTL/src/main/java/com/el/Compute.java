package com.el;

public class Compute {
	public static int sum(String x, String y) {
		
		int a = Integer.parseInt(x);
		int b = Integer.parseInt(y);
		return a + b;
	}
	public static int sub(String x, String y) {
		
		int a = Integer.parseInt(x);
		int b = Integer.parseInt(y);
		return a + b;
	}
}
